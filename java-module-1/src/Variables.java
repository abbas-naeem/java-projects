public class Variables {
    public static void main(String[] args) {
        // Integer
        int passangers = 6;
        System.out.println(passangers);

        // Updating Integer
        int busTickets = passangers;
        System.out.println(busTickets);

        // The max value is 2147483647 (Not needed for general info)
        int maxNumber = 2147483647;
        System.out.println(maxNumber);
    }
}
